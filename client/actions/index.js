// TYPES /////////////////////////////////////////////////////////////////////

const LOG_IN = '@@auth/SUBMIT_LOGIN_FORM';
const ADD = '@@test/ADD';
const SET_USER = '@@app/SET_USER';

// ACTIONS /////////////////////////////////////////////////////////////////////

// `setUser`
// 
// This function sets the current globallogged-in user to the application, if
// any
// 
// @params: user<Object>: the user object that has just logged in
// @returns: null
function setUser(user) {
  // TODO: replace with a dispatch
  // this.setState({ user });
  return {
    type: SET_USER,
    user
  }
}

function logIn(data) {
  return {
    type: LOG_IN,
    payload: data
  }
}

function add(num) {
  return {
    type: ADD,
    payload: num
  }
}

////////////////////////////////////////////////////////////////////////////////
// EXPORTS /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

module.exports = {
  types: {
    LOG_IN,
    ADD,
    SET_USER
  },
  actions: {
    logIn,
    add,
    setUser
  }
}
